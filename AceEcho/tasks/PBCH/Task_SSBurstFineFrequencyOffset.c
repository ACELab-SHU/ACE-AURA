  __v4096i16 cpProduct_imag;
  __v4096i16 cpProduct_tmpReal1;
  __v4096i16 cpProduct_tmpReal2;
  __v4096i16 cpProduct_tmpImag1;
  __v4096i16 cpProduct_tmpImag2;
  vsetshamt(fractionLength);
  cpProduct_tmpReal1 = vmul(vin_real_resample, delayed_real, MASKREAD_OFF, waveLength);
  cpProduct_tmpReal2 = vmul(vin_imag_resample, delayed_imag, MASKREAD_OFF, waveLength);
  // cpProduct_tmpReal1 = MUL4096_16_VV(vin_real_resample, delayed_real,
  //                                    fractionLength, MASKREAD_OFF,
  //                                    waveLength);
  // cpProduct_tmpReal2 = MUL4096_16_VV(vin_imag_resample, delayed_imag,
  //                                    fractionLength, MASKREAD_OFF,
  //                                    waveLength);
  vsetshamt(0);
  // VENUS_PRINTVEC_SHORT(cpProduct_tmpReal1, 257);
  cpProduct_real = vssub(cpProduct_tmpReal2, cpProduct_tmpReal1, MASKREAD_OFF, waveLength);
  // VENUS_PRINTVEC_SHORT(cpProduct_real, 257);
  vsetshamt(fractionLength);
  cpProduct_tmpImag1 = vmul(vin_real_resample, delayed_imag, MASKREAD_OFF, waveLength);
  cpProduct_tmpImag2 = vmul(vin_imag_resample, delayed_real, MASKREAD_OFF, waveLength);
  // cpProduct_tmpImag1 = MUL4096_16_VV(vin_real_resample, delayed_imag,
  //                                    fractionLength, MASKREAD_OFF,
  //                                    waveLength);
  // cpProduct_tmpImag2 = MUL4096_16_VV(vin_imag_resample, delayed_real,
  //                                    fractionLength, MASKREAD_OFF,
  //                                    waveLength);
  vsetshamt(0);
  cpProduct_imag = vsadd(cpProduct_tmpImag2, cpProduct_tmpImag1, MASKREAD_OFF, waveLength);
  // VENUS_PRINTVEC_SHORT(cpProduct_imag, 257);

  // Moving sum over 4 OFDM symbols (i.e. the size of the SS block)
  __v4096i16 sumTemp_real;
  __v4096i16 sumTemp_imag;
  vclaim(sumTemp_real);
  vshuffle(sumTemp_real, sumShuffleIndex, cpProduct_real, SHUFFLE_GATHER, 72);
  vclaim(sumTemp_imag);
  vshuffle(sumTemp_imag, sumShuffleIndex, cpProduct_imag, SHUFFLE_GATHER, 72);
  VENUS_PRINTVEC_SHORT(sumTemp_imag, 20);
  sumTemp_real                = vredsum(sumTemp_real, MASKREAD_OFF, 72);
  sumTemp_imag                = vredsum(sumTemp_imag, MASKREAD_OFF, 72);
  long long cpCorrResult_real = 0;
  long long cpCorrResult_imag = 0;
  int       sumTemp_real_addr = vaddr(sumTemp_real);
  VSPM_OPEN();
  cpCorrResult_real = *(volatile short *)(sumTemp_real_addr);
  printf("cpCorrResult_real:%hd\n", &cpCorrResult_real);
  // cpCorrResult_real = (cpCorrResult_real << 16) + *(volatile short
  // *)(sumTemp_real_addr + (1 << 1));
  cpCorrResult_real = (*(volatile short *)(sumTemp_real_addr + (1 << 1)) << 16) + cpCorrResult_real;
  // cpCorrResult_real = *(volatile short *)(sumTemp_real_addr + (1 <<
  // 1)); printf("cpCorrResult_real:%hd\n", &cpCorrResult_real);
  VSPM_CLOSE();
  int sumTemp_imag_addr = vaddr(sumTemp_imag);
  VSPM_OPEN();
  cpCorrResult_imag = *(volatile short *)(sumTemp_imag_addr);
  printf("cpCorrResult_imag:%hd\n", &cpCorrResult_imag);
  // cpCorrResult_imag = (cpCorrResult_imag << 16) + *(volatile short
  // *)(sumTemp_imag_addr + (1 << 1));
  cpCorrResult_imag = (*(volatile short *)(sumTemp_imag_addr + (1 << 1)) << 16) + cpCorrResult_imag;
  // cpCorrResult_imag = *(volatile short *)(sumTemp_imag_addr + (1 <<
  // 1)); printf("cpCorrResult_imag:%hd\n", &cpCorrResult_imag);
  VSPM_CLOSE();
  // printf("cpCorrResult_real:%hd\n", &cpCorrResult_real);
  // printf("cpCorrResult_imag:%hd\n", &cpCorrResult_imag);
  // VENUS_PRINTVEC_SHORT(sumTemp_real, 10);

  // TODO: angle
  long long x = cpCorrResult_real, y = cpCorrResult_imag;
  int       flag = 0;
  if (x < 0) {
    x = -x;
    flag |= (1 << 0);
  }
  if (y < 0) {
    y = -y;
    flag |= (1 << 1);
  }
  if (y > x) {
    long long tmp = y;
    y             = x;
    x             = tmp;
    flag |= (1 << 2);
  }
  long long  x_tan, y_tan, _y, _x;
  long long *ptan = (long long *)&_tan; //指针访问比数组访问快
  long long *pang = (long long *)&_angle;
  long long  res  = 0;

  /* 限制范围在uint16范围内,后续乘法不会超过uint32范围,防止溢出 */
  _y = (((long long)y << DEPTH) - 1) / x; //前面保证x>y,所以这里_y<65535
  _x = ((long long)1 << DEPTH) - 1;       //即为1/* 迭代计算 */

  for (int i = 0; i < DEPTH; i++) {
    //由于x_tan和y_tan有正有负,设置为int类型,但最大值为31位,故前面的tan用Q15来表示
    x_tan = (_x * (*ptan)) >> DEP_SHIFT; // Q15的tan * Q16的用户输入,计算后要右移Q15
    y_tan = (_y * (*ptan++)) >> DEP_SHIFT;
    if (_y > 0) {
      _x += y_tan;
      _y -= x_tan;
      res += *pang++;
    } else {
      _x -= y_tan;
      _y += x_tan;
      res -= *pang++;
    }
  }

  if (flag & 0x04) {
    switch (flag) {
    case 7:
      res = ((long long)270 << ANG_PRECICE) - res;
      break;
    case 4:
      res = ((long long)90 << ANG_PRECICE) - res;
      break;
    case 5:
      res = ((long long)90 << ANG_PRECICE) + res;
      break;
    case 6:
      res = ((long long)270 << ANG_PRECICE) + res;
      break;
    }
  } else {
    switch (flag) {
    case 0:
      break;
    case 1:
      res = ((long long)180 << ANG_PRECICE) - res;
      break;
    case 2:
      res = ((long long)360 << ANG_PRECICE) - res;
      break;
    case 3:
      res = ((long long)180 << ANG_PRECICE) + res;
      break;
    }
  }

  int result = (double)res * scs / 360 / (1 << ANG_PRECICE);
  //   printf("res:%hd\n", &result);
  vreturn(&result, sizeof(result));
